import Foundation
import RealmSwift

final class DataInitializer {

    func initData() {
        bulkInsert()
    }
}

private extension DataInitializer {

    func exportToUserDefaults() {
        let realmObjects = RealmDAO<FavoriteScorer>().findAll()
        // TODO: Arrayへの変換を除きたい
        let plainObjects = Array(realmObjects).map(FavoriteScorerPlain.init)
        UserDefaultsDAO().saveFavoriteScorers(plainObjects)
    }

    func replaceRealm() {
        let storageURL = Realm.Configuration.defaultConfiguration.fileURL!
        if FileManager.default.fileExists(atPath: storageURL.path) {
            return
        }

        let bundleURL = Bundle.main.url(forResource: "default", withExtension: "realm")!
        do {
            try FileManager.default.copyItem(at: bundleURL, to: storageURL)
        } catch {
            print(error.localizedDescription)
        }
    }

    func importFromUserDefaults() {
        let scorers = RealmDAO<Scorer>().findAll()
        UserDefaultsDAO().loadFavoriteScorers()
            .map { plainObject in
                let realmObject = FavoriteScorer()
                let scorer = scorers.first { $0.url == plainObject.url }
                realmObject.scorer = scorer
                realmObject.createdAt = plainObject.createdAt
                realmObject.lastReadAt = plainObject.lastReadAt
                realmObject.lastUpdatedAt = plainObject.lastUpdatedAt
                return realmObject
            }
            .forEach {
                RealmDAO<FavoriteScorer>().add(t: $0)
            }
    }

    func bulkInsert() {
        for association in associations {
            for competition in association.competitionsTemp {
                competition.setProperties(association: association)

                for scorer in competition.scorersTemp {
                    scorer.setProperties(competition: competition)
                }

                for overallScorer in competition.overallScorersTemp {
                    overallScorer.setProperties(competition: competition)
                }
            }

            for player in association.playersTemp {
                player.setProperties(association: association)
            }
        }

        let realm = try! Realm()

        try! realm.write {
            realm.add(associations)
            realm.add(associations.flatMap { $0.competitionsTemp })
            realm.add(associations.flatMap { $0.competitionsTemp }.flatMap { $0.scorersTemp })
            realm.add(associations.flatMap { $0.competitionsTemp }.flatMap { $0.overallScorersTemp })
            realm.add(associations.flatMap { $0.playersTemp })
        }

        let exportFileURL = Realm.Configuration.defaultConfiguration.fileURL!.deletingLastPathComponent().appendingPathComponent("exported.realm")
        try! realm.writeCopy(toFile: exportFileURL)
    }
}

